public class Method
{
    public static int addnumbers(int num1 , int num2)
    {
        return num1+ num2;
    }
    public static void main(String[] args)
    {
        int number1 = 100;
        int number2 = 17;
        int result = addnumbers(number1, number2);
        System.out.println(result);
    }
}